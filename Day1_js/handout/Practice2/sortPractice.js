'use strict'

function sort(arr) {
  //  return input.sort((a,b) => a-b); // Remove this line and change to your own algorithm

  var len = arr.length;
  for (var i = len - 1; i >= 0; i--) {
    for (var j = 1; j <= i; j++) {
      if (arr[j - 1] > arr[j]) {
        var temp = arr[j - 1];
        arr[j - 1] = arr[j];
        arr[j] = temp;
      }
    }
  }
  return arr;
}
module.exports = sort